    private void UpdatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatebtnActionPerformed
        // TODO add your handling code here:
                                                  
    int selectedRow = cashierTable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
        return;
    }

    // Retrieve existing values from the selected row
    String id = cashierTable.getValueAt(selectedRow, 0).toString();
    String name = JOptionPane.showInputDialog(this, "Enter new name:", cashierTable.getValueAt(selectedRow, 1).toString());
    String email = JOptionPane.showInputDialog(this, "Enter new email:", cashierTable.getValueAt(selectedRow, 2).toString());
    String mobile = JOptionPane.showInputDialog(this, "Enter new mobile number:", cashierTable.getValueAt(selectedRow, 3).toString());
    String password = JOptionPane.showInputDialog(this, "Enter new password:", cashierTable.getValueAt(selectedRow, 4).toString());
    String role = JOptionPane.showInputDialog(this, "Enter new role:", cashierTable.getValueAt(selectedRow, 5).toString());

    // Check if user inputs are valid
    if (name != null && email != null && mobile != null && password != null && role != null) {
        try {
            // Establish database connection
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafe", "root", "");
            
            // Update query with all required fields
            String query = "UPDATE user SET name=?, email=?, mobileNumber=?, password=?, role=? WHERE id=?";
            pst = con.prepareStatement(query);
            
            // Set the parameters for the prepared statement
            pst.setString(1, name);
            pst.setString(2, email);
            pst.setString(3, mobile);
            pst.setString(4, password);
            pst.setString(5, role);
            pst.setString(6, id);
            
            // Execute the update
            pst.executeUpdate();
            
            // Show confirmation message
            JOptionPane.showMessageDialog(this, "User updated successfully.");
            
            // Refresh the table to show updated data
            DisplayItems();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
    }


    }//GEN-LAST:event_UpdatebtnActionPerformed

